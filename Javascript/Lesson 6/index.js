/*learning about variable types. We have const (for constant variables that won't change), and let (for variables that will change). As you can see below, we've reassigned
one of the variables */

const firstName = 'Mike'
const favoriteColor = 'Green'
let currentLocation = 'Yum Town'
let currentMood = 'Happy'
currentMood = 'Hungies'

console.log('First Name - ' + firstName)
console.log('Favorite Color - ' + favoriteColor)
console.log('Current Location - ' + currentLocation)
console.log('Current Mood - ' + currentMood)
